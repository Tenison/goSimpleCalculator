package calculator

func Sum(firstNumb int, secondNum int) int{
	return firstNumb + secondNum
}

func Substract(firstNumb int, secondNum int) int{
	return firstNumb - secondNum
}

func Divide(firstNumb int, secondNum int) int{
	return firstNumb / secondNum
}

func Reminder(firstNumb int, secondNum int) int{
	return firstNumb % secondNum
}

func Multiplication(firstNumb int, secondNum int) int{
	return firstNumb * secondNum
}